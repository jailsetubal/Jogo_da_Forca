def solicita_chute():
    chute = input('Qual letra? ')
    chute = chute.strip().upper()
    return chute

def incrementa_chute(chute, letras_acertadas, palavra_secreta):
    index = 0
    for letra in palavra_secreta:
        if chute == letra:
            letras_acertadas[index] = letra
        index += 1


